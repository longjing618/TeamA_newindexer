package edu.buffalo.cse.irf14.document;

public class DocumentInfo {
	private String fileId;
	private int length;
	public String getFileId() {
		return fileId;
	}
	public void setFileId(String fileId) {
		this.fileId = fileId;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	
}
